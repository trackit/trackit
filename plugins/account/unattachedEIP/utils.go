package plugins_account_unattached_eip

import (
	"sync"

	"github.com/aws/aws-sdk-go/service/ec2"
)

type EIP struct {
	Region *string
	EIPRes *ec2.DescribeAddressesOutput
	Err    error
}

// merge function from https://blog.golang.org/pipelines#TOC_4
// It allows to merge many chans to one.
func merge(cs ...<-chan EIP) <-chan EIP {
	var wg sync.WaitGroup
	out := make(chan EIP)

	// Start an output goroutine for each input channel in cs. The output
	// copies values from c to out until c is closed, then calls wg.Done.
	output := func(c <-chan EIP) {
		for n := range c {
			out <- n
		}
		wg.Done()
	}
	wg.Add(len(cs))
	for _, c := range cs {
		go output(c)
	}

	// Start a goroutine to close out once all the output goroutines are
	// done. This must start after the wg.Add call.
	go func() {
		wg.Wait()
		close(out)
	}()
	return out
}
